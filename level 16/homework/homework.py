# ბულეანური ცვლადები
a = True
b = False
c = True

# AND ოპერატორი
print(a and b)  # შედეგი იქნება False, რადგან b არის False

# OR ოპერატორი
print(a or b)  # შედეგი იქნება True, რადგან a არის True

# OR ოპერატორი სხვა მაგალითით
print(b or c)  # შედეგი იქნება True, რადგან c არის True

# AND ოპერატორი 3 ბულეანზე
print(a and b and c)  # შედეგი იქნება False, რადგან b არის False

# OR ოპერატორი 3 ბულეანზე
print(a or b or c)  # შედეგი იქნება True, რადგან a და c არიან True


